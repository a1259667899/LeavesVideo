import Foundation
import ZKProgressHUD
class TBTHUD {
    /**
     只显示加载
     */
    class func showLoading(){
        ZKProgressHUD.show()
    }
    /**
     显示加载和tip
     */
    class func showLoadingWithText(message:String){
        ZKProgressHUD.show(message)
    }
    /**
     消失
     */
    class  func dismiss() {
        ZKProgressHUD.dismiss()
    }
    /**
     显示成功消息
     */
    class func showSuccessMessage(message:String){
        ZKProgressHUD.showSuccess(message)
    }
    /**
     显示失败消息
     */
    class func showErrorMessage(message:String){
        ZKProgressHUD.showError(message)
    }
    /**
     显示消息
     */
    class func showMessage(message:String){
        ZKProgressHUD.showMessage(message)
    }
}
